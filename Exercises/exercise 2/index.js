//-----Javascript Syntax-----//

// Variable - A variable is a container for storing data values

// var - Declares a variable, optionally initializing it to a value. [http://spoo.me/7d0p4UE]
var a = 10;

// let - Declares a block-scoped, local variable, optionally initializing it to a value. [http://spoo.me/VEEfS7P]
let b = 20;

// const - Declares a block-scoped, can't be changed through reassignment. [http://spoo.me/4hEu2yM]
const c = 30;

console.log(a);
console.log(b);
console.log(c);
